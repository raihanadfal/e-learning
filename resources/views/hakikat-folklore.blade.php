            <p>Apakah di wilayah kalian ada cerita yang turun-temurun yang kalian dengar? Entah itu dari keluarga, saudara, atau orang tua. Tentunya pasti ada saja cerita yang menarik di wilayah masing-masing. Nah, hal itu merupakan cerita yang berkembang di masyarakat sebagai cerita rakyat. Untuk lebih lengkapnya kita bahas bersama, apa itu folklore dan cerita rakyat.
              Folklor dapat ditinjau secara etimologi yang berasal bahasa Inggris folklore. Kata ini adalah kata majemuk, yang berasal dari dua kata folk dan lore. Folk merupakan suatu kelompok atau kolektif, yang dapat diartikan sekelompok orang yang memiliki ciri-ciri pengenal fisik, sosial, dan kebudayaan sehingga dapat dibedakan dari masing-masing kelompok. 
              Ciri-ciri pengenal itu, antara lain dapat berwujud warna kulit yang sama, bentuk rambut yang sama, mata pencaharian yang sama, bahasa yang sama, taraf pendidikan yang sama, dan agama yang sama. Mereka mempunyai suatu tradisi yaitu kebiasaan dan kebudayaan yang diwariskan secara turun-temurun dan diakui sebagai milik bersama. Mereka sadar akan identitas kelompoknya sendiri (Danandjaja, 1986: 1).
