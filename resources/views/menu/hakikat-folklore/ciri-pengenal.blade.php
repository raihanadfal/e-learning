          <h4>Macam-macam Folklore</h4>
          <p>
            Brunvand (Danandjaja, 1986: 21-22) membagi macam folklor menjadi tiga golongan besar, yaitu <b>folklor lisan</b>, <b>folklor sebagian lisan</b>, <b>dan folklor bukan lisan</b>. Folklor lisan adalah folklor yang bentuknya murni lisan salah satunya puisi rakyat dan cerita rakyat. Folklor sebagian lisan adalah folklor yang bentuknya merupakan campuran unsur bukan lisan, seperti tahayul, teater rakyat, permainan tradisional, upacara adat, tari dan pesta rakyat. Sedangkan folklor bukan lisan adalah folklor yang bentuknya bukan lisan yang dapat dikelompokkan berupa material dan bukan material. Folklor bukan lisan berupa material seperti arsitektur rakyat, hasil kerajinan tangan, pakaian, makanan, alat musik dan peralatan senjata. Sedangkan yang bukan material seperti bahasa isyarat dan musik (Danandjaja, 1986: 21-22).
