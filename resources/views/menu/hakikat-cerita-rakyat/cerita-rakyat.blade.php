              <h5>Fungsi Cerita Rakyat</h5>
              <p>Cerita rakyat yang diwariskan oleh nenek moyang tentu mempunyai fungsi tertentu. Apabila dikaji dapat dilihat adanya muatan nilai-nilai luhur, nilai-nilai moral dan dedaktik. Hal itu juga dapat dijadikan sebagai kontrol sosial dalam bertingkah laku maupun bertindak. Pada zaman dahulu, nenek moyang kita ketika menasehati anak dapat melalui cerita maupun tembang (Andalas, 2017: 5). Ada beberapa fungsi folklor yang dapat dijelaskan sebagai berikut.</p>
              <ul>
                <li>
                  <b>Penghibur</b>
                </li>
                <p>Adanya dongeng sebagai bagian dari folklor yang dapat mengibur pemilikinya. Dongeng sebagai salah satu jenis folklor tidak terlepas dari fungsi karya sastra, yaitu mendidik dan menghibur. Seperti dongeng kancil yang dapat menghibur pembaca ketika melihat tingkah dan kecerdikkannya.</p>
                <li>
                  <b>Alat Pendidikan</b>
                </li>
                <p>Folklor sebagai alat pendidikan dapat melalui cerita,permainan, maupun upacara adat. Dalam hal ini pendidikan yang didapat dari kisah-kisah yang dibawakan sehingga menjadi pelajaran untuk pembaca agar tidak melakukan kesalahan, durhaka, maupun melakukan pelanggaran yang dicerminkan melalui tokoh dan penokohan dari cerita tersebut.</p>
                <li>
                  <b>Kontrol Sosial</b>
                </li>
                <p>Kontrol sosial adalah cara yang atau sarana yang digunakan untuk mengendalikan tingkah laku masyarakat supaya mematuhi nilai-nilai dan kaidah yang berlaku di lingkungan sosial (Habib, 2012). Sedangkan menurut Suastika (2011: 35),”suatu sikap dari seseorang atau masyarakat yang peduli terhadap sesama dan lingkungannya. Melakukan berbagai penilaian yang kritis terhadap yang kurang baik menjadi lebih baik sesuai adat dan budaya masyarakat yang bersangkutan”. Jadi kontrol sosial tersebut terkait dengan pengendalian tingkah laku sesuai dengan tatanan sosial budaya masyarakat.</p>
                <li>
                  <b>Pemersatu</b>
                </li>
                <p>Folklor sebagai pemersatu dapat dipahami sebagai orang yang ingin mempersatukan atau alat untuk mempersatukan. Hal ini dapat dilihat dari cerita, tokoh, penokohan dan keseluruhan kepercayaan yang membuat pembaca atau pada masanya orang semakin yakin dan percaya pada kisah cerita tersebut.</p>
                <li>
                  <b>Pelestarian Lingkungan</b>
                </li>
