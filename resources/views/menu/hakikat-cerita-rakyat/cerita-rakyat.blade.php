              <h5>Cerita Rakyat</h5>
              <p>Cerita Rakyat adalah cerita yang berasal dari masyarakat dan berkembang dalam masyarakat pada masa lampau, juga menjadi ciri khas setiap bangsa yang memiliki kultur budaya yang beraneka ragam. Pada umumnya, cerita rakyat mengisahkan tentang suatu kejadian di suatu tempat atau asal muasal suatu tempat. Tokoh-tokoh yang dimunculkan dalam cerita rakyat umumnya diwujudkan dalam bentuk binatang, manusia, dan dewa (Gusnetti, 2015: 184).
                Menurut Hutomo (Emzir, 2009: 8) bahwa,”Cerita rakyat dapat diartikan sebagai ekspresi kesastraan warga suatu kebudayaan yang penyampaiannya dan penyebarannya disebarkan dan diinstrumenkan secara lisan yang berhubungan langsung dengan berbagai aspek budaya dan susunan nilai sosial masyarakat tersebut”. 
                Berdasarkan penjelasan di atas, dapat disimpulkan bahwa cerita rakyat merupakan bagian dari sastra lisan yang memiliki ciri-ciri disampaikan secara turun-temurun, berkembang dari mulut ke mulut, bersifat anonim, artinya nama pengarang tidak diketahui, memiliki bentuk-bentuk klise dalam susunan atau cara pengungkapannya, bersifat tradisional, memiliki nilai-nilai luhur, serta memiliki versi dan variasi yang berbeda sebab cerita rakyat disampaikan secara lisan. Hal ini mempengaruhi dari penyampaian kepada generasi berikutnya yang disebabkan dari kemampuan seseorang dalam menceritakan kembali cerita tersebut.
